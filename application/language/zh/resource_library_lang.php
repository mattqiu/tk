TPS不能为经销商提供任何关于个人所得税的建议。所有经销商应咨询自己的税务会计、税务律师或其他专业人员。

<h4>3.12 独立承包商身份</h4>
经销商为独立的承包商。TPS与其经销商之间达成的协议不能用于建立公司与该经销商之间的雇主/雇员关系、代理关系、合作伙伴关系以及合资关系。经销商提供服务时或支付联邦和国家征收的税务时，不得将其视为雇员。所有经销商均有责任支付地方、国家和联邦针对其作为公司经销商所赚取的全部薪酬征收的全部税款。TPS不负责扣留联邦社会保险税及其他各类税款，也不得从代表的奖金和佣金（若有）中扣除，除非扣留税款为合法要求。经销商无权也不会代表TPS或以其名义承担任何债务、费用支付和义务履行，也无权代表TPS或以其名义开设任何支票账户和信用卡。各个经销商应在遵守经销商协议和公司政策以及相关法律的基础上，确定各自的销售目标、销售时数和销售方式。各个经销商应全权负责支付发生的一切费用，包括但不限于差旅费、餐费、住宿费、文秘费、办公费、长途电话费及其他费用。

<h4>3.13 国际营销</h4>
根据公司官方材料的公布内容，经销商仅有权向在TPS有权进行业务经营的国家出售TPS产品及服务。此外，任何经销商均不能在任何未经授权的国家：（a）进行销售、招聘和召开会议等活动；（b）招聘或试图招聘潜在客户或经销商；（c）进行其他为赞助最完善解决方案产品或服务、创立营销组织或推销TPS机遇而需进行的活动。

<h4>3.14 招聘和奖金购买限制</h4>
各个经销商志愿加入公司并进行产品和服务购买。因此，严禁以任何理由从事以下活动包括“购买奖金”。禁止经销商：（a）招聘对独立经销商协议一无所知的个人或实体，和/或允许上述个人或实体执行独立经销商协议（包括但不限于通过合作方式）；（b）以诈骗方式将个人或实体招为经销商或客户；（c）招生或试图招生非存在个人或实体为经销商或客户（“代理人”）；（d）通过或代表不是信用卡持卡人的经销商或客户使用该信用卡；（e）为了合格获得佣金或奖金（包括但不限于），代表其他经销商或客户，或者使用其他经销商或客户的身份进行产品、服务及其他项目采购。经销商约定，自己不会仅仅为了合格获得薪酬计划下规定的薪酬而购买TPS产品和服务。任何经销商均不得影响或尝试影响任何其他经销商从事相同事宜。奖金购买还包括最终消费者欺瞒自己可合格获得不受真实产品或服务主导的名次提升、鼓励、奖品、佣金和奖金。

<h4>3.15 遵守法律和法令</h4>
经销商应在开展自己的业务期间遵守所有联邦、国家和地方法律和法规。许多城市和国家都有相关法律调控某些依托本国的业务。经销商必须遵守自己适用的法律。若有城市或国家官员告知经销商某项法令适用于该经销商，则该经销商应有礼貌并表示乐意合作地向相关法律顾问咨询此项法令的适用范围及合规性。

<h4>3.16 一个经销商仅只可以开一个TPS店铺，每个家庭只可以开三个店铺</h4>
在一项TPS业务中，经销商可以合资企业、合作伙伴、股东、受托人或受益人的身份，经营该业务，或具有合法、公正的所有者权益。任何个人均不可拥有和运作和提现一个以上店铺运转产生的奖金。同一家庭同一地址只可以允许开三个店铺。“家庭”定义为在同一地方居住或做生意，且有血缘、婚姻、国内合作伙伴或收养关系的各个成员，或以家庭为单位居住在一起或通过定居等方式生活在一个家庭中。

<h4>3.17 家庭成员或业务相关各方的纪律处分</h4>
如果有经销商的直属家庭中有成员参加了违背本协议规定的活动（若此次活动由经销商执行），则该经销商将把此次活动视作违法活动，同时TPS可按照本政策和程序的要求，对该经销商进行纪律处分。同样，如果有与公司、合作伙伴、有限责任公司、信托实体及其他合法实体（简称“业务实体”）以任何方式相关的个人违反了本协议，业务实体将把上述行为视作违法行为，同时TPS将对该经销商进行纪律处分。同样，若有经销商以业务实体的身份参加TPS，则该经销商的各个业务相关方（见第3.17.1节的定义）应亲自单独地不得不且必须遵守本协议的条款和条件。

<h5>3.17.1 生意实体</h5>
通过递交经销商申请和协议以及合理填写的生意实体即公司登记表和IRS表格-9（纳税人识别号申请），业务实体即公司可申请成为TPS经销商。若有经销商采用在线注册登记，则该经销商必须在在线登记之日起30天内，向TPS递交公司登记表（若在30天的期限内TPS未受到经销商协议，则该经销商协议应自动终止）。TPS业务状况可在同种赞助人之间变更，如从个人到公司。申请TPS业务状况变更所需费用为$25.00，其中必须包括递交书面申请和填写完整的经销商协议所需的费用。公司登记表必须由全体所有人、股东、成员、合作伙伴或受托人签字。根据TPS政策与程序、TPS经销商协议及TPS所有其他合同义务的要求，公司及其所有人、股东、成员、管理者、合作伙伴、受托人及其他具有该公司所有者权益或承担管理责任的各方（统称为“业务相关各方”）分别对TPS承担的任何债务负连带责任。最完善解决方案将只能认可一名受益人（依据会计业绩所得利益）。务必在填写公司登记表时指明上述利益的获得者（若有）。若未列出，则TPS将认为受益人为该表列举的第一个人。

<h4>3.18 独立TPS业务出售、转让或出让</h4>
尽管TPS业务为独立运作的业务，但是其出售、转让或出让却还是有些限制。如果有经销商希望出售自己的TPS业务，则该销售经销商必须为TPS公司总部提供该业务的优先购买权，购买具体根据与第三方（购买人）约定的相同条款。TPS应自收到经销商的书面提供之日起15天，可行使该优先购买权。若TPS选择不购买该业务，则想出售TPS店铺的经销商必须满足以下标准：<br><br>
•销售经销商必须向公司上交$50的转让费，以偿还其与该交易相关的所有费用。<br>
•TPS必须在确定最终购买之前核准购买人或受让人。<br>
•购买人或受让人必须为TPS合格经销商。若购买人为TPS现有经销商，则其必须首先终止自己的TPS业务，并等候六个日历月已获得不同TPS业务的利益。<br>
•销售经销商必须在TPS最终确定并认可出售、转让或出让之前，履行自己偿还所有与TPS之间产生的债务的义务。<br>
•销售经销商必须信誉良好，且不得违反本协议的任何条款，以便能有资格出售、转让或出让TPS业务。<br><br>

销售经销商在出售公司注册为经销商的独立TPS业务或利益时，必须书面通知TPS合规部，并告知其自己打算出售自己的TPS或自己以公司名字注册的TPS业务实体利益。继续进行出售之前，准备卖店铺的经销商必须收到TPS的书面许可。出售或转让的TPS业务将保持原推荐人。<br>

销售经销商约定，在出售或转让自己的TPS业务后，不以书面或口头形式，诽谤、贬低或批判TPS。经销商约定，无法确定因此类违规而使TPS遭受的实际损伤，并且也不切实际，或者极为困难。因此，若有此类违约发生，经销商在此约定，TPS可恢复造成的损伤，且不影响自己寻求禁令救济或其他公正救济方式的权利。

<h5>3.18.1 禁止融资</h5>
TPS不允许经销商针对自己的团队或潜在顾客使用任何融资途径或手段，其包括但不限于：经销商成员、产品或服务采购。一切采购事宜均需通过TPS进行，不得采用其他任何手段。违反本政策可能会造成经销商账户暂停使用或失效。

<h5>3.18.2 概述</h5>
各个经销商必须立即向TPS告知自己经销商协议或业务实体(公司)登记表上所含信息发生的全部变更。经销商可通过递交书面申请、合理执行的经销商协议和相关证明文件，修改其既有经销商协议（即，将社会安全号变更为联邦识别号，或将所有权形式从独资经营变更为由该经销商的业务实体所有）。变更处理只能每年进行一次。所有变更内容均须在11月30日之前递交，以便能在第二年1月1日生效。

