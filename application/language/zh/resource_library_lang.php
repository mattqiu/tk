<h4>3.20 在线赞助</h4>
赞助商通过在线注册过程赞助新经销商时，可帮助新申请者填写注册材料。新申请人必须亲自审阅并同意经销商协议。赞助商不可代表新申请人填写或递交经销商协议。

<h4>3.21 经销商的死亡或能力丧失</h4>
经销商协议为一份个人服务合同。经销商一旦死亡或丧失能力（根据公司自行确定），应立即执行经销商协议中的以下措施。<br>

为了有效遵照遗嘱转让TPS业务，遗产的执行人必须向TPS提供以下资料：（a）死亡证明原件；（b）遗嘱证明性或遗嘱执行人任命管理信；（c）授权遗嘱执行人为TPS发出的书面指示，规定业务和收入的受让人。<br>

因经销经销商丧失能力，为了有效转让TPS业务，继承人必须向TPS提供以下资料：（a）经公证的受托人任命书副本；（b）经公证的信托文件或其他建立受托人TPS业务管理权的文件的副本；（c）信托人执行的完整经销商协议。

<h4>3.22 电话营销技术</h4>
各个国家政府相关主管部门均有法律限制电话营销行为。美国联邦机构的电话营销法中均含有“谢绝来电”规章。尽管在传统意义上TPS未考虑到经销商成为“电话营销者”，但是，这些政府规章还是从广义上定义了“电话营销者”和“电话营销”，经销商拨打的电话号码如果在联邦的“谢绝来电”登记簿上，则有可能导致该经销商违法电话营销法。此外，切勿对这些规章掉以轻心，因为其中涉及重大处罚。<br>

因此，经销商在经营自己的TPS业务过程中，不得从事电话营销。“电话营销”一词的含义包括但不限于，一次或多次致电个人或实体，引诱其购买TPS服务，招聘个人或实体利用TPS机遇。向潜在客户或经销商拨打的、为其推销TPS产品或服务或使其利用TPS机遇的“冷不防电话”可能构成电话营销，需予以禁止。<br>

在某些情况下，若经销商与潜在客户建立了业务关系，则可允许其打电话。经销商负责理解并遵守这些法律，并确定允许打电话的时间。<br>

对于TPS业务的经营，经销商不得利用自动电话拨号系统或软件。经销商不得致电针对TPS产品、服务或机遇发送了预先录制的信息或“自动语言电话”的任何个人，也不得发起任何海外通话。


<h4>3.23 后台访问</h4>
TPS应使在线后台为其经销商所用。在线后台是仅可用于发展经销商的TPS业务、增加TPS产品和服务销售的保密信息。然而，访问后台是一种特权，而非权利。 TPS 保留自行决定拒绝经销商访问后台的权利。因后台信息的保密性质，永远不得公开披露后台中包含的信息，包括信息截图或任何衍生物。<br>
如果一个经销商通过注册过程将其API密匙交给另一个经销商，得到信息的经销商不得以任何理由共享和出售该信息。如果发现经销商违反此政策，其账户将立即被暂停，且经销商关系可能被终止。

<h4>3.24 联系方式变更</h4>
为确保产品、服务、辅助资料、佣金和税收文件的及时交付，TPS后台经销商的资料应为当前最新的。如经销商变更任何联系方式或搬家，必须通过TPS网站的后台功能更新其联系方式。为确保交付，应提前两周向TPS发出通知，说明所有变更情况。

<h4>3.25 持续发展义务</h4>
<h5>3.25.1 持续培训</h5>
为TPS保荐另一名经销商的任何经销商时，必须为其提供善意的帮助和培训，确保他或她的营销组织可使其TPS商业正确运营。经销商必须与其营销组织中的经销商进行持续的接触和沟通。这种接触和沟通可包括但不限于：时事通讯、书面信函、个人会议、电话联系、语音邮件、电子邮件和与陪同新经销商参加TPS当地会议、培训性会议和其他。经销商领导还应负责对新经销商进行TPS产品和服务知识、有效销售技巧、TPS酬金方案和遵守公司政策和程序方面的激励和培训。与新经销商的沟通和对新经销商的培训不得违反第 3.1 和/或第3.2节的规定（有关发展经销商-­‐提供销售支持和宣传材料）。<br>
经销商领导应适当监控其营销组织中的新经销商，防止新经销商制造不当产品、获取不正当收益、要求商业索赔或从事任何非法或不当行为。

<h5>3.25.2 增大的培训责任</h5>
随着在各级领导下的进步发展，经销商将在销售技巧、产品和服务知识和理解完美解决方案方面更有经验。他们可能会被要求与其营销组织中经验较少的经销商分享其知识。

<h5>3.25.3 持续销售责任</h5>
无论成就如何，经销商均有一个持续的义务——通过产生新客户和服务现有客户的方式亲自进行推销。

<h4>3.26 非贬低公司</h4>
TPS重视来自经销商的建设性的批评和意见。所有这些意见均应以书面形式提交给法务部（legal@tps138.com）。同时，TPS欢迎经销商就其公司、产品、酬金方案发表建设性的输入，负面评论和意见，以防止其他TPS经销商失去热情。由于这一原因以及为给其营销组织树立正确的榜样，经销商不得蔑视贬低TPS、其他TPS 经销商、 TPS的产品或服务、酬金方案或TPS主管、官员和员工、或对其发表负面评价。

<h4>3.27 产品销售及销售佣金</h4>
TPS奖金方案基于将产品和服务销售给最终消费者。经销商必须完成TPS奖金方案中规定的个人销售要求（并满足协议中规定的其他职责），以获得从TPS产品和服务销售中获得佣金的资格。所有佣金应通过每个经销商激活和保留的付款处理账户直接支付给合格的经销商。

